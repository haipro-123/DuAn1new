    private void btnTimKiemMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnTimKiemMouseClicked
        // TODO add your handling code here:
        if (txtTenTimKiem.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Nhap ten tim kiem");
        } else {
            List<Voucher> list = new ArrayList<>();
            for (Voucher vou : listvoucher) {
                if (vou.getTen().equalsIgnoreCase(txtTenTimKiem.getText().trim())) {
                    list.add(vou);
                }
            }
            showDataTableVoucher(list);
        }
    }//GEN-LAST:event_btnTimKiemMouseClicked


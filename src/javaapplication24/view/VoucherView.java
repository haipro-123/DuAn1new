    private void btnThemMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnThemMouseClicked
        // TODO add your handling code here:
        if (kiemTraDuLieu()) {
            Voucher vou = new Voucher(txtTen.getText(), (byte) (cboLoai.getSelectedIndex()), Integer.parseInt(txtGiatri.getText()), Integer.parseInt(txtHSD.getText()), Integer.parseInt(txtDkNhan.getText()), Integer.parseInt(txtMucGiaGioiHan.getText()), txtNgayKetThuc.getText());
            if (vs.add(vou)) {
                JOptionPane.showMessageDialog(this, "add thanh cong");
                listvoucher = vs.getList();
                showDataTableVoucher(listvoucher);
            } else {
                JOptionPane.showMessageDialog(this, "add that bai");
            }
        }
    }//GEN-LAST:event_btnThemMouseClicked

    private void btnSuaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnSuaMouseClicked
        int row = tblVoucher.getSelectedRow();
        if (row >= 0 && row < listvoucher.size() && kiemTraDuLieu()) {
            String id = listvoucher.get(row).getId();
            Voucher vou = new Voucher(id, txtTen.getText(), (byte) (cboLoai.getSelectedItem().toString().equalsIgnoreCase("Theo %") ? 0 : 1), Integer.parseInt(txtGiatri.getText()), Integer.parseInt(txtHSD.getText()), Integer.parseInt(txtDkNhan.getText()), Integer.parseInt(txtMucGiaGioiHan.getText()), txtNgayKetThuc.getText());
            if (vs.update(vou)) {
                JOptionPane.showMessageDialog(this, "update thanh cong");
                listvoucher = vs.getList();
                showDataTableVoucher(listvoucher);
            } else {
                JOptionPane.showMessageDialog(this, "update that bai");
            }
        }
    }//GEN-LAST:event_btnSuaMouseClicked


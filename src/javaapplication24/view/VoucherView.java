    private void showDataTableVoucher(List<Voucher> listvoucher) {
        dtmv.setRowCount(0);
        for (Voucher vou : listvoucher) {
            dtmv.addRow(new Object[]{vou.getTen(), vou.getLoai() == 0 ? "Theo %" : "Theo $", vou.getGiaTri(), vou.getHsd(), vou.getDkNhan(), vou.getMucGiaGioiHan(), vou.getNgayKetThuc()});
        }
    }

    private boolean kiemTraDuLieu() {
        if (txtTen.getText().trim().isEmpty() || txtGiatri.getText().trim().isEmpty() || txtHSD.getText().trim().isEmpty() || txtDkNhan.getText().trim().isEmpty() || txtGiatri.getText().trim().isEmpty() || txtMucGiaGioiHan.getText().trim().isEmpty() || txtNgayKetThuc.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Nhap du du lieu");
            return false;
        } else if (kiemTraKieuInt(txtGiatri.getText()) || kiemTraKieuInt(txtHSD.getText()) || kiemTraKieuInt(txtDkNhan.getText()) || kiemTraKieuInt(txtMucGiaGioiHan.getText())) {
            JOptionPane.showMessageDialog(this, "Nhap du lieu so");
            return false;
        }
        return true;
    }

    private boolean kiemTraKieuInt(String text) {
        int t;
        try {
            t = Integer.parseInt(text);
        } catch (NumberFormatException e) {
            return true;
        }
        return t <= 0;
    }

    private void showDataTableVoucherChiTiet(List<VoucherChiTiet> listvct) {
        dtmvct.setRowCount(0);
        for(VoucherChiTiet vct :listvct){
            dtmvct.addRow(new Object[]{vct.getIdVoucherChiTiet(),vct.getIdVoucher(),vct.getIdKhachHang(),vct.getNgayBatDau(),getTrangThai(vct.getTrangThai()),vct.getIdHoaDon()});
        }
    }

    private String getTrangThai(byte trangThai) {
        return switch (trangThai) {
            case 0 -> "Chua su dung";
            case 1 -> "Da su dung";
            case 2 -> "het han";
            default -> null;
        };
    }
